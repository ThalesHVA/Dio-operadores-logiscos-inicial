class ninja {
    constructor(name,age,clan,ataque){
        this.name = name;
        this.age = age;
        this.clan = clan;
        this.ataque = ataque;
    }
}
let chunin = new ninja("Shikamaru","16","Nara","Posseção das Sombras")
console.log(chunin["name","age","clan"])
console.log("seu ataque é " + chunin["ataque"])
